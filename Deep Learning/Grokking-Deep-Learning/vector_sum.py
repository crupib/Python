def vector_sum(vec_a):
    """
    Calculate the sum of all elements in a vector.

    Args:
        vec_a (list or tuple): The input vector.

    Returns:
        float or int: The sum of all elements in the vector.
    """
    return sum(vec_a)

# Example usage
vec_a = [1, 2, 3, 4]
result = vector_sum(vec_a)
print(result)  # Output: 10

